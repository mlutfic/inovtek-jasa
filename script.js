function resetScrollToTopOnRefresh() {
  if ("scrollRestoration" in window.history) {
    window.history.scrollRestoration = "manual";
  }

  const jumpToTop = () => {
    window.scrollTo(0, 0);
  };

  if (window.location.hash) {
    window.history.replaceState(null, "", `${window.location.pathname}${window.location.search}`);
  }

  window.addEventListener("load", jumpToTop);
  window.addEventListener("pageshow", jumpToTop);
}

function createPriceCard(plan) {
  const card = document.createElement("article");
  card.className = `price-card${plan.featured ? " is-featured" : ""}`;

  const badge = plan.badge
    ? `<div class="price-badge">${plan.badge}</div>`
    : "";

  const features = plan.features.map((item) => `<span>${item}</span>`).join("");

  card.innerHTML = `
    ${badge}
    <h3>${plan.name}</h3>
    <p class="price-value">${plan.price}</p>
    <p class="price-note">${plan.note}</p>
    <div class="price-features">${features}</div>
    <a class="price-button" href="#contact">Pilih Paket</a>
  `;

  return card;
}

