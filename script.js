function renderPortfolio(key) {
  if (!portfolioGrid) {
    return;
  }

  const cards =
    portfolioCatalog[key] ?? portfolioCatalog["website-profile"] ?? [];

  portfolioGrid.replaceChildren(...cards.map(createPortfolioCard));
  initScrollReveal(portfolioGrid);
}

function renderTabs(activeKey) {
  if (!pricingTabs) {
    return;
  }

  const tabs = pricingCatalog.map((category) => {
    const button = document.createElement("button");
    const isActive = category.key === activeKey;

    button.type = "button";
    button.className = `tab-button${isActive ? " is-active" : ""}`;
    button.dataset.pricing = category.key;
    button.setAttribute("role", "tab");
    button.setAttribute("aria-selected", String(isActive));
    button.textContent = category.label;

    return button;
  });

  pricingTabs.replaceChildren(...tabs);
}

function renderPortfolioCategoryOptions(activeKey) {
  if (!portfolioCategorySelect) {
    return;
  }

  const options = pricingCatalog.map((category) => {
    const option = document.createElement("option");
    option.value = category.key;
    option.textContent = category.label;
    option.selected = category.key === activeKey;

    return option;
  });

  portfolioCategorySelect.replaceChildren(...options);
}

function renderPricing(key) {
  const category =
    pricingCatalog.find((item) => item.key === key) ?? pricingCatalog[0];

  renderTabs(category.key);
  renderPortfolioCategoryOptions(category.key);
  renderPortfolio(category.key);

  if (!pricingGrid || !pricingTitle || !pricingDescription) {
    return;
  }

  pricingTitle.textContent = category.title;
  pricingDescription.textContent = category.description;
  pricingGrid.dataset.planCount = String(category.plans.length);
  pricingGrid.replaceChildren(...category.plans.map(createPriceCard));
  initScrollReveal(pricingGrid);
}

