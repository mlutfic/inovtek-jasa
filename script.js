function formatTrustCounterValue(value, suffix = "") {
  return `${new Intl.NumberFormat("id-ID").format(value)}${suffix}`;
}

function primeTrustCounters() {
  trustCounters.forEach((counter) => {
    if (counter.dataset.counted === "true") {
      return;
    }

    counter.textContent = formatTrustCounterValue(0, counter.dataset.suffix || "");
  });
}

function animateTrustCounters() {
  if (!trustCounters.length) {
    return;
  }

  trustCounters.forEach((counter) => {
    if (counter.dataset.counted === "true") {
      return;
    }

    const target = Number(counter.dataset.target || 0);
    const suffix = counter.dataset.suffix || "";
    const duration = 1400;
    const startTime = performance.now();

    counter.dataset.counted = "true";

    const tick = (now) => {
      const progress = Math.min((now - startTime) / duration, 1);
      const eased = 1 - (1 - progress) ** 3;
      const currentValue = Math.round(target * eased);

      counter.textContent = formatTrustCounterValue(currentValue, suffix);

      if (progress < 1) {
        requestAnimationFrame(tick);
        return;
      }

      counter.textContent = formatTrustCounterValue(target, suffix);
    };

    requestAnimationFrame(tick);
  });
}

primeTrustCounters();

function collectRevealTargets(scope = document) {
  return [
    ...scope.querySelectorAll(
      [
        ".hero-copy",
        ".hero-showcase",
        ".trust-panel",
        ".section-heading",
        ".service-card",
        ".pricing-context",
        ".price-card",
        ".portfolio-header",
        ".portfolio-card",
        ".benefit-card",
        ".testimonial-card",
        ".cta-panel",
        ".footer-grid",
        ".footer-bottom",
      ].join(",")
    ),
  ];
}

function initScrollReveal(scope = document) {
  const targets = collectRevealTargets(scope).filter(
    (element) => !element.classList.contains("reveal-ready")
  );

  if (!targets.length) {
    return;
  }

  targets.forEach((element, index) => {
    element.classList.add("reveal-ready");

    if (
      element.matches(
        ".service-card, .price-card, .portfolio-card, .benefit-card, .testimonial-card"
      )
    ) {
      element.style.setProperty("--reveal-delay", `${Math.min(index % 6, 5) * 70}ms`);
    }
  });

  if (
    !("IntersectionObserver" in window) ||
    window.matchMedia("(prefers-reduced-motion: reduce)").matches
  ) {
    targets.forEach((element) => element.classList.add("is-visible"));
    animateTrustCounters();
    return;
  }

  const observer = new IntersectionObserver(
    (entries, activeObserver) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) {
          return;
        }

        entry.target.classList.add("is-visible");

        if (entry.target.matches(".trust-panel")) {
          animateTrustCounters();
        }

        activeObserver.unobserve(entry.target);
      });
    },
    {
      root: null,
      rootMargin: "0px 0px -8% 0px",
      threshold: 0.12,
    }
  );

  targets.forEach((element) => observer.observe(element));
}

initScrollReveal();
